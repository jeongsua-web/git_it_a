# 팀실습과제 1번
t.shape("turtle")
t.up()
t.goto(-200,-100)
t.down()
t.fillcolor("red")
t.begin_fill()
t.forward(400)
t.left(90)
t.forward(200)
t.left(90)
t.forward(400)
t.left(90)
t.forward(200)
t.end_fill()

t.up()
t.goto(-100,100)
t.down()
t.fillcolor("yellow")
t.begin_fill()
t.left(180)
t.forward(100)
t.right(90)
t.forward(200)
t.right(90)
t.forward(100)
t.end_fill()

t.up()
t.goto(-50,-100)
t.down()
t.fillcolor("black")
t.begin_fill()
t.left(180)
t.circle(50)
t.end_fill()

t.up()
t.goto(50,-100)
t.down()
t.fillcolor("black")
t.begin_fill()
t.right(180)
t.circle(50)
t.end_fill()

t.reset()
# 팀실습과제 2번
t.shape("turtle")
t.up()
t.goto(-100, -100)
t.down()

t.fillcolor("yellow")
t.begin_fill()
t.fd(200)
t.left(90)
t.fd(200)
t.left(90)
t.fd(200)
t.left(90)
t.fd(200)
t.end_fill()

t.goto(-100, 100)

t.fillcolor("green")
t.begin_fill()

t.left(150)
